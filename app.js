let  n1=7;
let  n2=3;


function restar(num1,num2){
    let resultado= num1-num2;
    console.log(`El resultado de la resta es ${resultado}`);
    return resultado;
}

function sumar(num1,num2){
    let resultado= num1+num2;
    console.log(`El resultado de la suma es ${resultado}`);
    return resultado;
}

sumar(n1,n2);